x = int(input("Enter an integer:"))
if x < 0: #checking if x is less than 0
    x = 0
    print('Negative changed to zero')
elif x == 0: # short form of elseif as elseif
    print('Zero')
elif x == 1: # short form of elseif as elseif
    print('Single')
else: # end condition check
    print('More')   

